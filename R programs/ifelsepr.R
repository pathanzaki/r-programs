# simple if else 

x <- 20
if (x > 30) {
  print("x is greater than 10")
} else {
  print("x is less from 10")
}

# if else ladder

score <- 95

if (score >= 90) {
  print("Grade: A")
} else if (score >= 75) {
  print("Grade: B")
} else if (score >= 60) {
  print("Grade: C")
} else {
  print("Grade: F")
}
